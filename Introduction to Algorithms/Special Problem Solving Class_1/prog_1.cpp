void solve()
{
    int n, m;
    cin >> n >> m;

    vector<int> a(n + 1);
    for (int i = 1; i <= n; i++)
        cin >> a[i];

    vector<int> cnt(n + 1);

    set<int> s;
    for (int i = n; i > 0; i--)
    {
        s.insert(a[i]);
        cnt[i] = s.size();
    }

    while (m--)
    {
        int l;
        cin >> l;

        cout << cnt[l] << endl;
    }
}

int32_t main()
{
    ios::sync_with_stdio(0);
    cin.tie(0);

    int t = 1;
    // cin >> t; // Uncomment if multiple test cases

    while (t--)
    {
        solve();
    }

    return 0;
}
